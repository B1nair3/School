class Car {
  String _owner = "";
  String _model = "";
  String _isTunned = "";
  String _motorPower = "";
  String _useCarbonPlating = "";
  String _renewTire = "";
  bool _isPriorized = false;
  
  get owner => _owner;

 set owner( value) => _owner = value;

  get model => _model;

 set model( value) => _model = value;

  get isTunned => _isTunned;

 set isTunned( value) => _isTunned = value;

  get motorPower => _motorPower;

 set motorPower( value) => _motorPower = value;

  get useCarbonPlating => _useCarbonPlating;

 set useCarbonPlating( value) => _useCarbonPlating = value;

  get renewTire => _renewTire;

 set renewTire( value) => _renewTire = value;

  get isPriorized => _isPriorized;

 set isPriorized( value) => _isPriorized = value;
}

